CREATE TABLE score_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    risk_score DOUBLE PRECISION NOT NULL,
    review_readiness_score DOUBLE PRECISION NOT NULL,
    risk_category VARCHAR(20) NOT NULL,
    merge_recommendation VARCHAR(20) NOT NULL,
    overridden BOOLEAN NOT NULL DEFAULT FALSE,
    override_note TEXT,
    pull_request_record_id BIGINT NOT NULL UNIQUE REFERENCES pull_request_records(id),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_score_results_pr_id ON score_results(pull_request_record_id);
CREATE INDEX idx_score_results_risk_category ON score_results(risk_category);
